CREATE TABLE VOTING (
                ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
                ASSOCIATE VARCHAR(100) NOT NULL,
                VOTE boolean NOT NULL,
                ID_SESSION BIGINT NOT NULL,
                CONSTRAINT PK_VOTING PRIMARY KEY (ID)
);
CREATE INDEX IX_VOTING_ID ON VOTING(ID);

ALTER TABLE VOTING ADD CONSTRAINT FK_VOTING_X_SESSION
FOREIGN KEY (ID_SESSION)
REFERENCES SESSION (ID)
ON DELETE NO ACTION
ON UPDATE NO ACTION;

CREATE UNIQUE INDEX UK_VOTING
 ON VOTING
 ( ID_SESSION, ASSOCIATE );